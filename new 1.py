# Colors List
colors = ["Red", "Blue", "Green", "Yellow"]
print(f"Size of list: {len(colors)}")

# Numeric List Operations
numbers = [10, 20, 30, 40, 50]
print(f"Greatest: {max(numbers)}, Minimum: {min(numbers)}")
print(f"Sum of elements: {sum(numbers)}")

# List Comprehension: Add 5 to each element
added_five = [x + 5 for x in numbers]
print(f"List after adding 5: {added_five}")
